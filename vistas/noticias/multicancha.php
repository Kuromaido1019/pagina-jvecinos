                <div>
                    <p>Más de 47 millones de pesos fue el monto invertido en mejorar la deteriorada multicancha de las
                        villas
                        Don Mateo 1 y 2, del sector norte de Rancagua, lo cual viene a mejorar la calidad de vida de sus
                        vecinos, quienes encuentran en el recinto un lugar para compartir y disfrutar de actividades
                        deportivas
                        y de convivencia.</p>
                    <p>El proyecto fue financiados con el Programa de Mejoramiento Urbano de la Subsecretaría de
                        Desarrollo
                        Regional (SUBDERE), y permitió la demolición de la antigua losa de hormigón y la construcción de
                        una
                        nueva de 619 metros cuadrados. Además, se mejoró la estructura del cierre perimetral,
                        aumentándose la
                        altura de 3 a 4 metros y se reemplazó la deteriorada malla metálica por una nueva.</p>
                    <p>Durante las obras, se instalaron arcos nuevos de babyfútbol y soportes para vóleibol, con sus
                        correspondientes redes, y se reparó las estructuras de los tableros y aros de básquetbol.
                        También se
                        pintó la losa y se marcaron las canchas para practicar diferentes disciplinas deportivas, y se
                        incorporó
                        un sistema de iluminación LED.</p>
                    <p>El Alcalde Rancagua, Eduardo Soto, destacó que "acogimos la petición de los vecinos y buscamos el
                        financiamiento para realizar esta obra, con un proyecto que elevó los estándares que existían,
                        en cuento
                        a iluminación y la terminación del espacio deportivo, que es lo que hemos venido replicando en
                        otros
                        sectores de la ciudad. Nos motiva el hecho que estas multicanchas no solo son ocupadas por los
                        jóvenes,
                        sino que por los padres y adultos mayores, que realizan actividades recreativas, deportivas y de
                        convivencia, por lo que estas multicanchas son un espacio de encuentro de todos los vecinos y
                        por eso
                        hacemos los esfuerzos por que los proyectos se materialicen y repliquen en diferentes sectores
                        de
                        Rancagua".</p>
                    <p>Por su parte, el presidente de la Junta de Vecinos de Villa Don Mateo 1 y 2, Luis Hernán
                        Oyarzún,indicó
                        que "estamos muy contentos con este logro, tuvimos que esperar con paciencia, pero gracias a
                        Dios todo
                        resultó bien, por lo que agradecemos la gestión de las autoridades, porque gracias al trabajo de
                        todos
                        estamos disfrutando de esta renovada multicancha de nuestra villa".</p>
                    <p>"Además del deporte, acá nosotros realizamos diferentes actividades culturales, teatro, ferias,
                        aniversarios, hemos hecho un gran trabajo con nuestros vecinos para promover las actividades
                        musicales y
                        artísticas, con el fin de que todos participen y aprovechemos esta multicancha", recalcó Luis
                        Oyarzún.
                    </p>
